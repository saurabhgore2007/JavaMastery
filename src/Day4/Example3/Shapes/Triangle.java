package Day4.Example3.Shapes;

public class Triangle {

	private float base;
	private float height;
	
	
	public Triangle() {
		this.base = 0.0f;
		this.height = 0.0f;
	}
	
	public Triangle(float base,float height) {
		this.base = base;
		this.height = height;
	}
	
	public float getBase() {
		return base;
	}
	
	public float getHeight() {
		return base;
	}
}
